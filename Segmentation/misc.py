import numpy as np


def to_original_shape(data, dataset):

    orig_shape = dataset['original_shape']
    [minZ, maxZ, minX, maxX, minY, maxY] = dataset['bbox_brain']

    new_seg = np.zeros(orig_shape, dtype=data.dtype)
    new_seg[minZ:maxZ, minX:maxX, minY:maxY] = data

    return new_seg

